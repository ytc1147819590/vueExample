<template>
  <div class="hello">
    <h1>{{ msg }}</h1>
    <form>
      <input type="text" name="username" v-model="userName"> <br>
      <input type="text" name="age" v-model="age"> <br>
      <a href="javascript:;" @click="addUser">提交</a>
    </form>
  </div>
</template>

<script>
  export default {
    name: 'hello',
    data () {
      return {
        msg: 'Welcome to Your Vue.js App',
        userName: '',
        age: ''
      }
    },
    methods: {
      addUser() {
        var name = this.userName;
        var age = this.age;

        this.$axios({
          url:"/api/user/addUser",
          method:"post",
          data:{
            username: name,
            age:age
          }
        }).then(function(res){
          console.log(res);
        }).catch(function(err){
          console.log(err);
        })
      }
    }
  }
</script>
